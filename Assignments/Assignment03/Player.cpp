void CMyPlayer::Update(float fTimeElapsed)
{
	if (dwDirection && xRail == xNextRail)
	{
		XMFLOAT3 xmf3Shift = XMFLOAT3(0, 0, 0);
		if (dwDirection & DIR_FORWARD) {}
		if (dwDirection & DIR_BACKWARD) {}
		if (dwDirection & DIR_RIGHT) {
			if (xRail < 4)
			{
				xNextRail = xRail + 1;
				Rotate(0.0f, +15.0f, 0.0f);
			}
			dwDirection = 0;
		}
		if (dwDirection & DIR_LEFT) {
			if (xRail > 0)
			{
				xNextRail = xRail - 1;
				Rotate(0.0f, -15.0f, 0.0f);
			}
			dwDirection = 0;
		}
		if (dwDirection & DIR_UP) {}
		if (dwDirection & DIR_DOWN) {}
	}

	tz += speed * fTimeElapsed;
	if (tz > 1.0f)
	{
		tz -= 1.0f;
		zRail = zNextRail;
		zNextRail += 1;
		UpdateCount += 1;
		if (zNextRail == 300)
			zNextRail = 0;
	}
	if (UpdateCount == 100)
	{
		NeedUpdate = true;
		UpdateCount = 0;
	}


	if (xRail != xNextRail)
		tx += speed * fTimeElapsed;

	if (tx > 1.0f)
	{
		tx = 0.0f;
		if (xRail < xNextRail)
			Rotate(0.0f, -15.0f, 0.0f);
		else
			Rotate(0.0f, +15.0f, 0.0f);
		m_xmf3Position.x = m_Track[xNextRail]->GetPosition().x;
		xRail = xNextRail;
	}

	if (m_Track)
	{
		int indexNow = 5 * int(zRail);
		int indexNext = 5 * int(zNextRail);

		XMFLOAT3 resultPosition = {};
		resultPosition.y = m_xmf3Position.y;
		resultPosition.z = (1.0f - tz) * m_Track[indexNow]->GetPosition().z + tz * m_Track[indexNext]->GetPosition().z;

		if (xRail != xNextRail)
		{
			indexNow = int(xRail);
			indexNext = int(xNextRail);

			resultPosition.x = (1.0f - tx) * m_Track[indexNow]->GetPosition().x + tx * m_Track[indexNext]->GetPosition().x;
		}
		else
			resultPosition.x = m_xmf3Position.x;

		SetPosition(resultPosition);
	}

	if (m_pPlayerUpdatedContext) OnPlayerUpdateCallback(fTimeElapsed);

	DWORD nCurrentCameraMode = m_pCamera->GetMode();
	if (nCurrentCameraMode == THIRD_PERSON_CAMERA) m_pCamera->Update(m_xmf3Position, fTimeElapsed);
	if (m_pCameraUpdatedContext) OnCameraUpdateCallback(fTimeElapsed);
	if (nCurrentCameraMode == THIRD_PERSON_CAMERA) m_pCamera->SetLookAt(m_xmf3Position);
	m_pCamera->RegenerateViewMatrix();

}

CMyCamera* CMyPlayer::OnChangeCamera(DWORD nNewCameraMode, DWORD nCurrentCameraMode)
{
	CMyCamera* pNewCamera = NULL;
	switch (nNewCameraMode)
	{
	case FIRST_PERSON_CAMERA:
		pNewCamera = new CFirstCarCamera(m_pCamera);
		break;
	case THIRD_PERSON_CAMERA:
		pNewCamera = new CThirdCarCamera(m_pCamera);
		break;
	case SPACESHIP_CAMERA:
		pNewCamera = new CCarCamera(m_pCamera);
		break;
	}
	if (nCurrentCameraMode == CAR_CAMERA)
	{
		m_xmf3Right = Vector3::Normalize(XMFLOAT3(m_xmf3Right.x, 0.0f, m_xmf3Right.z));
		m_xmf3Up = Vector3::Normalize(XMFLOAT3(0.0f, 1.0f, 0.0f));
		m_xmf3Look = Vector3::Normalize(XMFLOAT3(m_xmf3Look.x, 0.0f, m_xmf3Look.z));

		m_fPitch = 0.0f;
		m_fRoll = 0.0f;
		m_fYaw = Vector3::Angle(XMFLOAT3(0.0f, 0.0f, 1.0f), m_xmf3Look);
		if (m_xmf3Look.x < 0.0f) m_fYaw = -m_fYaw;
	}
	else if ((nNewCameraMode == CAR_CAMERA) && m_pCamera)
	{
		m_xmf3Right = m_pCamera->GetRightVector();
		m_xmf3Up = m_pCamera->GetUpVector();
		m_xmf3Look = m_pCamera->GetLookVector();
	}

	if (pNewCamera)
	{
		pNewCamera->SetMode(nNewCameraMode);
		pNewCamera->SetPlayer(this);
	}

	if (m_pCamera) delete m_pCamera;

	return(pNewCamera);
}

void CMyPlayer::OnPrepareRender()
{
	m_xmf4x4Transform._11 = m_xmf3Right.x; m_xmf4x4Transform._12 = m_xmf3Right.y; m_xmf4x4Transform._13 = m_xmf3Right.z;
	m_xmf4x4Transform._21 = m_xmf3Up.x; m_xmf4x4Transform._22 = m_xmf3Up.y; m_xmf4x4Transform._23 = m_xmf3Up.z;
	m_xmf4x4Transform._31 = m_xmf3Look.x; m_xmf4x4Transform._32 = m_xmf3Look.y; m_xmf4x4Transform._33 = m_xmf3Look.z;
	m_xmf4x4Transform._41 = m_xmf3Position.x; m_xmf4x4Transform._42 = m_xmf3Position.y; m_xmf4x4Transform._43 = m_xmf3Position.z;

	UpdateTransform(NULL);
}

