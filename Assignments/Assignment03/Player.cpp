void CCarPlayer::Animate(float fTimeElapsed, XMFLOAT4X4* pxmf4x4Parent)
{
	if (m_pBLFrame)
	{
		XMMATRIX xmmtxRotate = XMMatrixRotationX(XMConvertToRadians(360.0f * 2.0f) * fTimeElapsed);
		m_pBLFrame->m_xmf4x4Transform = Matrix4x4::Multiply(xmmtxRotate, m_pBLFrame->m_xmf4x4Transform);
	}
	if (m_pBRFrame)
	{
		XMMATRIX xmmtxRotate = XMMatrixRotationX(XMConvertToRadians(360.0f * 4.0f) * fTimeElapsed);
		m_pBRFrame->m_xmf4x4Transform = Matrix4x4::Multiply(xmmtxRotate, m_pBRFrame->m_xmf4x4Transform);
	}

	if (m_pFLFrame)
	{
		XMMATRIX xmmtxRotate = XMMatrixRotationX(XMConvertToRadians(360.0f * 2.0f) * fTimeElapsed);
		m_pFLFrame->m_xmf4x4Transform = Matrix4x4::Multiply(xmmtxRotate, m_pFLFrame->m_xmf4x4Transform);
	}
	if (m_pFRFrame)
	{
		XMMATRIX xmmtxRotate = XMMatrixRotationX(XMConvertToRadians(360.0f * 4.0f) * fTimeElapsed);
		m_pFRFrame->m_xmf4x4Transform = Matrix4x4::Multiply(xmmtxRotate, m_pFRFrame->m_xmf4x4Transform);
	}

	CMyPlayer::Animate(fTimeElapsed, pxmf4x4Parent);
}

